import * as utils from 'forgae-utils'

type INetworkParams = {
    url: string;
    networkId: string;
}

export class Network implements INetworkParams{
    networkId: any;
    compilerUrl: any;
    url: string;

    constructor (_network: string) {
        let params: INetworkParams = this.getNetwork(_network);
        this.url = params.url;
        this.networkId = params.networkId;
    }

    private getNetwork(_network: string): INetworkParams {
        let params: INetworkParams = utils.getNetwork(_network);
        return params;
    }
}
